/**
 * All built-in pixel fonts.
 */
export const builtInFonts: PixelFont[] = [font3x5, font5x7];

/**
 * Default font ID.
 */
export const defaultFontId = 'small-5x7';

/**
 * Get a font by its ID.
 */
export function getFont(fontId: string): PixelFont | undefined {
  return builtInFonts.find(f => f.id === fontId);
}

/**
 * Get the default font.
 */
export function getDefaultFont(): PixelFont {
  return font5x7;
}

/**
 * Calculate the pixel width of a text string using a given font.
 */
export function measureText(text: string, font: PixelFont): number {
  if (text.length === 0) return 0;
  return text.length * font.charWidth + (text.length - 1) * font.charSpacing;
}

/**
 * Render text to a canvas context at the specified position.
 * @param ctx - Canvas 2D rendering context
 * @param text - Text string to render
 * @param x - X position (in pixels)
 * @param y - Y position (in pixels)
 * @param font - Pixel font to use
 * @param color - Fill color (hex string or any valid CSS color)
 */
export function renderText(
  ctx: CanvasRenderingContext2D,
  text: string,
  x: number,
  y: number,
  font: PixelFont,
  color: string
): void {
  ctx.fillStyle = color;

  let cursorX = x;

  for (const char of text) {
    const charData = font.chars[char];
    if (charData) {
      // Render the character pixel by pixel
      for (let row = 0; row < charData.length; row++) {
        for (let col = 0; col < charData[row].length; col++) {
          if (charData[row][col] === 1) {
            ctx.fillRect(cursorX + col, y + row, 1, 1);
          }
        }
      }
    }
    // Move to next character position
    cursorX += font.charWidth + font.charSpacing;
  }
}

/**
 * Get the x position of a cursor at a given character index.
 * @param text - The text string
 * @param cursorIndex - Character index (0 = before first char, text.length = after last char)
 * @param font - Pixel font being used
 * @param textX - X position of the text start
 * @returns X position of the cursor in canvas pixels
 */
export function getCursorX(
  _text: string,
  cursorIndex: number,
  font: PixelFont,
  textX: number
): number {
  if (cursorIndex === 0) return textX;
  return textX + cursorIndex * (font.charWidth + font.charSpacing) - font.charSpacing;
}

/**
 * Render a cursor (blinking line) at the specified position.
 * @param ctx - Canvas 2D rendering context
 * @param x - X position
 * @param y - Y position (top of text)
 * @param height - Height of cursor (usually font.charHeight)
 * @param color - Cursor color
 */
export function renderCursor(
  ctx: CanvasRenderingContext2D,
  x: number,
  y: number,
  height: number,
  color: string
): void {
  ctx.fillStyle = color;
  ctx.fillRect(x, y, 1, height);
}
